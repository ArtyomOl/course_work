def _simple_stem(word: str) -> str:
    if len(word) < 4:
        return word
    
    word = re.sub(r'(.)\1+', r'\1', word)
    word_endings = _load_word_endings()
    sorted_endings = sorted(word_endings, key=len, reverse=True)

    for ending in sorted_endings:
        if word.endswith(ending) and len(word) - len(ending) >= 3:
            return word[:-len(ending)]
    
    return word

def preprocess_text(text: str) -> str:
    if not text or not isinstance(text, str):
        return ""
    
    text = text.lower()
    text = re.sub(r'[^а-яёa-z\s]', ' ', text)
    text = re.sub(r'\s+', ' ', text)
    
    words = text.split()
    stop_words = _load_russian_stop_words()
    words = [word for word in words if word not in stop_words and len(word) > 2]

    stemmed_words = []
    for word in words:
        stemmed_word = _simple_stem(word)
        stemmed_words.append(stemmed_word)
    
    stemmed_words = [word for word in stemmed_words if len(word) > 1]
    
    result = ' '.join(stemmed_words)
    
    return result.strip()
